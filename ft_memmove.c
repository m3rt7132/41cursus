#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	char	temp[n];
	char	*d;
	const char	*s;
	size_t	i;

	i = 0;
	s = (const char *)src;
	d = (char *)dest;
	if (d == s || n == 0)
		return (dest);
	while (i < n)
	{
		temp[i] = s[i];
		i++;
	}
	i = 0;
	while (i < n)
	{
		d[i] = temp[i];
		i++;
	}
	return (dest);
}
